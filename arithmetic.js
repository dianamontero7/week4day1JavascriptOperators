// Part 2 - Subtraction
var slicesOfPizza = 8;
var slicesEaten = 3;

var slicesLeft = slicesOfPizza - slicesEaten;

var slicesLeft;
console.log("Slices of pizza left:", slicesLeft); // DO NOT MODIFY THIS LINE

//========================================================================================

// Part 3 - Multiplication
var familyMembers = 4;
var movieTicketPrice = 10.50;

var totalCost = familyMembers * movieTicketPrice;

var totalCost;
console.log("Price to take the family to the movies:", totalCost); // DO NOT MODIFY THIS LINE

//========================================================================================

// Part 4 - Division
var totalCandies = 20;
var numberOfStudents = 4;

var candiesPerStudent = totalCandies / numberOfStudents;

var candiesPerStudent;
console.log("Each student gets this many candies:", candiesPerStudent); // DO NOT MODIFY THIS LINE

//========================================================================================

// Part 5 - Modulus (Remainder)
var totalCookies = 23;
